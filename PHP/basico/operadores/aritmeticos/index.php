<?php

    // Operadores Aritméticos o Matemáticos

    // + Suma
    $a = 10;
    $b = 5;
    $suma = $a + $b;
    echo "La suma de $a + $b es: $suma <br>";

    // - Resta
    $a = 10;
    $b = 5;
    $resta = $a - $b;
    echo "La resta de $a - $b es: $resta <br>";

    // * Multiplicación
    $a = 10;
    $b = 5;
    $multiplicacion = $a * $b;
    echo "La multiplicación de $a * $b es: $multiplicacion <br>";

    // / División
    $a = 10;
    $b = 5;
    $division = $a / $b;
    echo "La división de $a / $b es: $division <br>";

    // % Módulo o Resto
    $a = 10;
    $b = 3;
    $modulo = $a % $b;
    echo "El módulo de $a % $b es: $modulo <br>";

    // ** Potenciación
    $a = 10;
    $b = 2;
    $potencia = $a ** $b;
    echo "La potencia de $a ** $b es: $potencia <br>";

    // ++ Incremento
    $a = 10;
    $a++;
    echo "El incremento de $a es: $a <br>";

    // -- Decremento
    $a = 10;
    $a--;
    echo "El decremento de $a es: $a <br>";