<?php

    // Operadores de incremento y decremento

    // Incremento
    $a = 10;
    $b = 10;
    // Pre-incremento
    echo ++$a; // Muestra 11
    // Post-incremento
    echo $b++; // Muestra 10

    // Decremento
    $c = 10;
    $d = 10;
    // Pre-decremento
    echo --$c; // Muestra 9
    // Post-decremento
    echo $d--; // Muestra 10