<?php

    // Operadores de Asignación

    // Asignación simple
    $a = 10; // Asignación de valor 10 a la variable $a
    
    // Asignación compuesta
    $a += 5; // Asignación de $a + 5 a la variable $a (equivalente a $a = $a + 5)
    $a -= 3; // Asignación de $a - 3 a la variable $a (equivalente a $a = $a - 3)
    $a *= 2; // Asignación de $a * 2 a la variable $a (equivalente a $a = $a * 2)
    $a /= 4; // Asignación de $a / 4 a la variable $a (equivalente a $a = $a / 4)
    $a %= 3; // Asignación de $a % 3 a la variable $a (equivalente a $a = $a % 3)

    // Asignación de cadenas
    $str = "Hola"; // Asignación de la cadena "Hola" a la variable $str
    $str .= " Mundo"; // Concatenación de la cadena " Mundo" a la variable $str (equivalente a $str = $str . " Mundo")

    // Imprimir resultados
    echo "Valor de a: $a\n"; // Imprime el valor de $a
    